# レセプト種別コード(4桁)の構成
#   1桁目: 1=医科
#   2桁目: 1=単独 2=2併 3=3併 4=4併以上
#   3桁目: 1=本人 2=未就学者 3=家族 4=高齢受給者一般・低所得 5=高齢受給者7割 (公費単独等は 0/8/9系)
#   4桁目: 1=入院 2=入院外
