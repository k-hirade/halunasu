RECEIPT_TYPE_DIGIT3 = {
    "1": "本人",
    "2": "未就学者",
    "3": "家族",
    "4": "高齢受給者・一般/低所得",
    "5": "高齢受給者・7割給付",
    "6": "高齢受給者外",  # 予備的
    "8": "高齢者一般",
    "0": "公費",
}
RECEIPT_TYPE_DIGIT4 = {"1": "入院", "2": "入院外"}


def describe_receipt_type(code: str) -> str:
    """レセプト種別コードの説明文字列"""
    if len(code) != 4:
        return f"不正な種別({code})"
    parts = []
    parts.append("医科" if code[0] == "1" else f"点数表{code[0]}")
    parts.append(RECEIPT_TYPE_DIGIT2.get(code[1], f"併用区分{code[1]}"))
    parts.append(RECEIPT_TYPE_DIGIT3.get(code[2], f"区分{code[2]}"))
    parts.append(RECEIPT_TYPE_DIGIT4.get(code[3], f"入外{code[3]}"))
    return "・".join(parts)


