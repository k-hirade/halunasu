from pathlib import Path

OUT_DIR = Path(__file__).parent


def _item(rec, shikibetsu, code, quantity, tensu, kaisu, days):
    row = [
        rec, shikibetsu, "1", code,
        "" if quantity is None else str(quantity),
        "" if tensu is None else str(tensu),
        str(kaisu),
    ]
    row += [""] * 6  # コメント3組
    day_fields = [""] * 31
    for d, c in (days or {}).items():
        day_fields[d - 1] = str(c)
    return row + day_fields


def si(shikibetsu, code, tensu, kaisu, days, quantity=None):
    """SIレコード(診療行為・44項目)。days={日: 回数}"""
    return _item("SI", shikibetsu, code, quantity, tensu, kaisu, days)


def iy(shikibetsu, code, quantity, tensu, kaisu, days):
    """IYレコード(医薬品・44項目)"""
    return _item("IY", shikibetsu, code, quantity, tensu, kaisu, days)


def sy(code, start, tenki="1", modifiers="", name="", main=False):
    """SYレコード(傷病名・8項目)"""
    return ["SY", code, start, tenki, modifiers, name, "01" if main else "", ""]


def re_rec(no, type_code, ym, name, sex, birth, karte):
    """REレコード(先頭14項目まで記録)"""
    return ["RE", str(no), type_code, ym, name, sex, birth, "", "", "", "", "", "", karte]


def ho(insurer, kigo, bango, days, total):
    return ["HO", insurer, kigo, bango, str(days), str(total)]


def build_file(path, seikyu_ym, receipts):
    """receipts: [(RE行, HO行, [SY行...], [SI/IY行...])]"""
    lines = [["IR", "1", "22", "1", "9990001", "", "デモクリニック", seikyu_ym, "00", "0541234567"]]
    total_points = 0
    for re_row, ho_row, sys_, items in receipts:
        lines.append(re_row)
        lines.append(ho_row)
        lines.extend(sys_)
        lines.extend(items)
        total_points += int(ho_row[5])
    lines.append(["GO", str(len(receipts)), str(total_points), "99"])
    text = "\r\n".join(",".join(row) for row in lines) + "\r\n"
    path.write_bytes(text.encode("cp932") + b"\x1a")
    print(f"{path.name}: {len(receipts)}件 {total_points:,}点")


